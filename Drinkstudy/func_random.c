relogio* randomVector(int shots, int max) {
    relogio *v = (relogio *)malloc(sizeof(relogio) * shots);

    for (int i = 0; i < shots; i++) {
        v[i].h = 0;
        v[i].m = 0 + rand() % max;
        v[i].s = 0 +rand() % 59;
    }
    for (int i = 0; i < shots - 1; i++) {
        for (int j = 0; j < shots - 1 - i; j++) {

            int tempo1 = v[j].h * 3600 + v[j].m * 60 + v[j].s;
            int tempo2 = v[j+1].h * 3600 + v[j+1].m * 60 + v[j+1].s;

            if (tempo1 > tempo2) {
                relogio temp = v[j];
                v[j] = v[j+1];
                v[j+1] = temp;
            }
        }
    }

    return v;
}

